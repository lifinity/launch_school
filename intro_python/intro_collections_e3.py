# differences
# lists are mutable, tuples immutable
# list literals use [] vs tuple literals use ()
# need comma to init single element tuple e.g. (1,); not true for lists, [1] works

# similarities
# lists & tuples both sequence types (ordered), heterogenous and indexable
# can both be initialized using list()/tuple() & list(iterator)/tuple(iterator)